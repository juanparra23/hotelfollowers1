package com.mycompany.hotelfollowers.kitchen;

/**
 *
 * @author estudiantelis
 */
public class FoodItem {
    
    private int id;
    private String name;

    public FoodItem() {
        
        id = 0;
        name = "";
    }

    public FoodItem(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    
}
